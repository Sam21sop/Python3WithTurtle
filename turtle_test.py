import turtle 
t = turtle.Turtle()
t.position() # current position
t.forward(25) # moving forward
t.fd() # show the direction of turtle
t.backward(10) # moving backward
t.bk(10) #moving backword
t.back(5) #moving backward

#change shape of turtle
t.shape('turtle')
t.shape('arrow')
t.shape('classic')
t.shape('circle')
t.shape('square')
t.shape('triangle')
